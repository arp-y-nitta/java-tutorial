package 基礎編02.写経問題;

public class Calc {
	public static void main(String[] args) {
		int num = 10;
		
		System.out.println("変数numの値は: 10");
		System.out.println("加算結果: " + (num + 5));
		System.out.println("減算結果: " + (num - 3));
		System.out.println("乗算結果: " + (num * 2));
		System.out.println("除算結果: " + (num / 2));
	}
}
