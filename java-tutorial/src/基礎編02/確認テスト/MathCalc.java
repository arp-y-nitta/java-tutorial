package 基礎編02.確認テスト;
//演算子と式問題
public class MathCalc {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		
		System.out.println("num1とnum2の和は: " + (num1 + num2));
		System.out.println("num1とnum2の差は: " + (num1 - num2));
		System.out.println("num1とnum2の積は: " + (num1 * num2));
		System.out.println("num1とnum2の商は: " + (num1 / num2));
		System.out.println("num1とnum2の剰余は: " + (num1 % num2));
	}

}
